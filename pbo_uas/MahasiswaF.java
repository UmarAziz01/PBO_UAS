    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("yang dipilih : " + filePilihan.getAbsolutePath());

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                String baris;
                String pemisah = ";";

                while ((baris = br.readLine()) != null) {
                    String[] data = baris.split(pemisah);
                    String nim = data[0];
                    String nama = data[1];
                    String alamat = data[2];
                    String asalSMA = data[3];
                    String namaOrtu = data[4];

                    if (!nim.isEmpty() && !nama.isEmpty() && !alamat.isEmpty() && !asalSMA.isEmpty() && !namaOrtu.isEmpty()) {
                        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_UAS1PU");
                        EntityManager em = emf.createEntityManager();
                        em.getTransaction().begin();

                        Mahasiswa satu = new Mahasiswa();

                        satu.setNim(nim);
                        satu.setNama(nama);
                        satu.setAlamat(alamat);
                        satu.setAsalsma(asalSMA);
                        satu.setNamaortu(namaOrtu);

                        em.persist(satu);

                        em.getTransaction().commit();

                        em.close();
                        loadDataPU();
                        tfNIM.setEditable(true);
                    } else {
                        JOptionPane.showMessageDialog(null, "Gagal diinput");
                    }
                }
                JOptionPane.showMessageDialog(null, "Sukses diinput");
                loadDataPU();
            } catch (FileNotFoundException ex) {
                Logger.getLogger(MahasiswaF.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Gagal diinput");
            } catch (IOException ex) {
                Logger.getLogger(MahasiswaF.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Gagal diinput");
            }
        }
    }//GEN-LAST:event_btnUploadActionPerformed

