    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_UAS1PU");
        EntityManager em = emf.createEntityManager();
        String nim, nama, alamat, asalSMA, namaOrtu;
        nim = tfNIM.getText();
        nama = tfNama.getText();
        alamat = tfAlamat.getText();
        asalSMA = tfAsalSMA.getText();
        namaOrtu = tfNamaOrtu.getText();

        em.getTransaction().begin();

        Mahasiswa mhs = em.find(Mahasiswa.class, nim);
        if (mhs == null) {
            JOptionPane.showMessageDialog(null, "Data tidak ditemukan");
        } else {
            mhs.setNim(nim);
            mhs.setNama(nama);
            mhs.setAlamat(alamat);
            mhs.setAsalsma(asalSMA);
            mhs.setNamaortu(namaOrtu);

            em.getTransaction().commit();
            JOptionPane.showMessageDialog(null, "Data dengan NIM " + tfNIM.getText() + ", berhasil diupdate");

            em.close();

            clearForm();
        }

        loadDataPU();
        tfNIM.setEditable(true);
    }//GEN-LAST:event_btnUpdateActionPerformed


    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBO_UAS1PU");
        EntityManager em = emf.createEntityManager();
        String NIM = tfNIM.getText();
        Mahasiswa Mahasiswa = em.find(Mahasiswa.class, NIM);
        if (Mahasiswa == null) {
            System.out.println("Mahasiswa dengan NIM: " + NIM + " tidak ada");
        } else {
            em.getTransaction().begin();
            em.remove(Mahasiswa);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(this, "Hapus mata kuliah dengan NIM : " + tfNIM.getText() + ", berhasil dihapus");

        }
        em.close();
        loadDataPU();
        clearForm();
        tfNIM.setEditable(true);
    }//GEN-LAST:event_btnHapusActionPerformed

