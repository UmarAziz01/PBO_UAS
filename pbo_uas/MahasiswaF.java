    public void loadDataPU() {
        EntityManager em = Persistence.createEntityManagerFactory("PBO_UAS1PU").createEntityManager();

        Query query = em.createNamedQuery("Mahasiswa.findAll");
        List<Mahasiswa> hasil = query.getResultList();

        DefaultTableModel tbl = new DefaultTableModel(new String[]{"NIM", "Nama", "Alamat", "Asal SMA", "Nama Ortu"}, 0);
        for (Mahasiswa data : hasil) {
            Object[] baris = new Object[5];
            baris[0] = data.getNim();
            baris[1] = data.getNama();
            baris[2] = data.getAlamat();
            baris[3] = data.getAsalsma();
            baris[4] = data.getNamaortu();
            tbl.addRow(baris);
        }
        tblMahasiswa.setModel(tbl);
    }

